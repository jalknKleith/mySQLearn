--to see the number of values.
SELECT COUNT(colName) FROM tableName;

--to see the number of rows that matches a specified criterion.
SELECT COUNT(colName) FROM tableName WHERE condition;

--to see the average of values.
SELECT AVG(colName) FROM tableName;

--to see the average value of a numeric column.
SELECT AVG(colName) FROM tableName WHERE condition;

--to see the sum of values.
SELECT SUM(colName) FROM tableName;

--to see the total sum of a numeric column.
SELECT SUM(colName) FROM tableName WHERE condition;
